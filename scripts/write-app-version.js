const { execFileSync } = require('child_process');
const fs = require('fs');
const path = require('path');

const repoRoot = path.resolve(__dirname, '..');
const versionConfigPath = path.join(repoRoot, 'app-version.json');
const allTargetFiles = {
  admin: path.join(repoRoot, 'admin', 'src', 'config', 'appVersion.js'),
  agents: path.join(repoRoot, 'agents', 'src', 'config', 'appVersion.js'),
};

const getTargetFiles = () => {
  const cwd = path.resolve(process.cwd());

  if (cwd === path.join(repoRoot, 'admin')) return [allTargetFiles.admin];
  if (cwd === path.join(repoRoot, 'agents')) return [allTargetFiles.agents];

  return Object.values(allTargetFiles);
};

const readGit = (args, fallback) => {
  try {
    const value = execFileSync('git', args, {
      cwd: repoRoot,
      encoding: 'utf8',
      stdio: ['ignore', 'pipe', 'ignore'],
    }).trim();

    return value || fallback;
  } catch (error) {
    return fallback;
  }
};

const hasPendingChanges = () => readGit(['status', '--porcelain'], '') !== '';

const readMajorVersion = () => {
  const config = JSON.parse(fs.readFileSync(versionConfigPath, 'utf8'));
  const major = Number(process.env.APP_MAJOR_VERSION || config.major || 1);

  if (!Number.isInteger(major) || major < 1) {
    throw new Error('app-version.json must define "major" as an integer greater than 0.');
  }

  return major;
};

const wait = (milliseconds) => {
  const end = Date.now() + milliseconds;

  while (Date.now() < end) {
    // Wait for Windows file locks during parallel builds.
  }
};

const writeFileIfChanged = (targetFile, content) => {
  if (fs.existsSync(targetFile) && fs.readFileSync(targetFile, 'utf8') === content) {
    return false;
  }

  let lastError;

  for (let attempt = 0; attempt < 5; attempt += 1) {
    try {
      fs.writeFileSync(targetFile, content);
      return true;
    } catch (error) {
      lastError = error;

      if (!['EPERM', 'EBUSY'].includes(error.code)) {
        throw error;
      }

      wait(100 * (attempt + 1));
    }
  }

  throw lastError;
};

const major = readMajorVersion();
const headCommitCount = Number(readGit(['rev-list', '--count', 'HEAD'], '0'));
const commitCount = headCommitCount + (hasPendingChanges() ? 1 : 0);
const appVersion = `v${major}.${commitCount}`;

const fileContent = `// Auto-generated by scripts/write-app-version.js. Do not edit by hand.
export const APP_VERSION = '${appVersion}';
export const APP_VERSION_MAJOR = ${major};
export const APP_COMMIT_COUNT = ${commitCount};

export default APP_VERSION;
`;

getTargetFiles().forEach((targetFile) => {
  fs.mkdirSync(path.dirname(targetFile), { recursive: true });
  writeFileIfChanged(targetFile, fileContent);
});

console.log(`App version generated: ${appVersion}`);
